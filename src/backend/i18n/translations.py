SUPPORTED_LANGUAGES = list(TRANSLATIONS.keys())


def get_translation(lang: str, key: str) -> str:
    """Get translation for a key in given language, fallback to English."""
    lang = lang.lower()[:2]
    if lang not in TRANSLATIONS:
        lang = "en"
    return TRANSLATIONS[lang].get(key, TRANSLATIONS["en"].get(key, key))


def get_translations_bundle(lang: str) -> dict:
    """Get full translation bundle for a language."""
    lang = lang.lower()[:2]
    if lang not in TRANSLATIONS:
        lang = "en"
    return {"language": lang, "translations": TRANSLATIONS[lang]}
